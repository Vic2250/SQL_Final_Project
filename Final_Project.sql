--SELECT

--員工資訊
SELECT EMP_ID AS 員工ID, (EMP_LNAME + EMP_FNAME) AS 員工姓名, EMP_BIRTH AS 員工生日, EMP_REIGON AS 員工國籍, EMP_PHONE AS 員工電話, EMP_EMAIL AS 員工電子郵件 
FROM EMPLOYEE

--員工職位
SELECT EMPLOYEE.EMP_ID AS 員工ID, (EMPLOYEE.EMP_LNAME + EMPLOYEE.EMP_FNAME) AS 員工姓名, JOB.JOB_NAME AS 職位,DEPARTMENT.DEP_NAME AS 部門, JOB_HISTORY.STARTS_DATE AS 開始工作間
FROM EMPLOYEE INNER JOIN (JOB_HISTORY INNER JOIN(JOB INNER JOIN DEPARTMENT ON JOB.DEP_ID = DEPARTMENT.DEP_ID)
  ON JOB_HISTORY.JOB_ID = JOB.JOB_ID)
  ON EMPLOYEE.JOB_ID = JOB_HISTORY.JOB_ID
ORDER BY DEPARTMENT.DEP_NAME

--最高薪資
SELECT (EMP_LNAME + EMP_FNAME) AS 最高薪水員工, SALARY_NUM AS 最高薪資
FROM EMPLOYEE, SALARY
WHERE SALARY.SALARY_NUM = (SELECT MAX(SALARY_NUM) FROM SALARY) AND EMPLOYEE.JOB_ID = SALARY.JOB_ID
ORDER BY SALARY_NUM DESC

--優秀員工獎次數
SELECT (EMP_LNAME + EMP_FNAME) AS 員工姓名,TOP_TIME AS 優秀員工獎次數
FROM JOB_HISTORY, EMPLOYEE
WHERE EMPLOYEE.EMP_ID = JOB_HISTORY.EMP_ID
ORDER BY TOP_TIME DESC

--平均薪資
SELECT (EMP_LNAME + EMP_FNAME) AS 在1月31前平均薪資大於平均員工姓名, SALARY_NUM AS 薪資
FROM EMPLOYEE, SALARY
WHERE SALARY_NUM >= (SELECT AVG(SALARY_NUM) FROM SALARY WHERE SALARY_TIME <= '01/31/2022') AND SALARY.JOB_ID = EMPLOYEE.JOB_ID  AND SALARY.SALARY_TIME <= '01/31/2022'
ORDER BY SALARY_NUM DESC

--印出證照、職位，以及姓名，按員工ID排列
SELECT (EMP_LNAME + EMP_FNAME) AS 員工姓名,JOB_NAME AS 職位 ,LIENSE_NAME AS 執照
FROM EMPLOYEE INNER JOIN JOB ON EMPLOYEE.JOB_ID = JOB.JOB_ID INNER JOIN (EXP_LIENSE INNER JOIN LIENSE ON EXP_LIENSE.LIENSE_ID = LIENSE.LIENSE_ID)  ON EMPLOYEE.EMP_ID = EXP_LIENSE.EMP_ID
ORDER BY EMPLOYEE.EMP_ID


--沒唸研究所的人或有工作經歷
SELECT (EMP_LNAME + EMP_FNAME) AS 員工姓名, GRADUATE_SCHOOL AS 畢業研究所, JOB_NAME AS 職位
FROM (EMPLOYEE FULL JOIN EXP_DO ON EMPLOYEE.EMP_ID = EXP_DO.EMP_ID FULL JOIN EXP_SCHOOL ON EMPLOYEE.EMP_ID = EXP_SCHOOL.EMP_ID FULL JOIN JOB ON JOB.JOB_ID = EMPLOYEE.JOB_ID ) 
WHERE  EXP_SCHOOL.GRADUATE_SCHOOL IS NULL OR EMPLOYEE.EMP_ID =EXP_DO.EMP_ID
ORDER BY EXP_SCHOOL.GRADUATE_SCHOOL

--台北市中山區部門及員工
SELECT CITY, DEP_NAME , (EMP_LNAME + EMP_FNAME) AS 員工姓名
FROM EMPLOYEE INNER JOIN (JOB INNER JOIN (DEPARTMENT INNER JOIN LOCATIONS ON DEPARTMENT.LOCATION_ID = LOCATIONS.LOCATION_ID AND LOCATIONS.CITY = '臺北市中山區')ON JOB.DEP_ID = DEPARTMENT.DEP_ID)ON EMPLOYEE.JOB_ID = JOB.JOB_ID
ORDER BY DEPARTMENT.DEP_NAME

--印出考核低於3的員工、考核時間、考核回饋
SELECT (EMP_LNAME + EMP_FNAME) AS 員工姓名, ASSESSMENT.ASSESS_SCORE AS 考核分數, ASSESSMENT.ASSESS_TIME AS 考核公布時間, ASSESSMENT.ASSESS_COMMENTS AS 考核回饋
FROM EMPLOYEE, ASSESSMENT
WHERE EMPLOYEE.EMP_ID = ASSESSMENT.EMP_ID AND ASSESSMENT.ASSESS_SCORE <= 3


--部門名稱包含'金融'而且員工編號在11010到11020之間
SELECT EMP_ID AS 員工編號 ,(EMP_LNAME + EMP_FNAME) AS 員工姓名, JOB_NAME, MIN_SALARY
FROM JOB INNER JOIN EMPLOYEE ON JOB.JOB_ID = EMPLOYEE.JOB_ID INNER JOIN SALARY ON JOB.JOB_ID = SALARY.JOB_ID
WHERE JOB.JOB_NAME LIKE'%金融%' AND EMPLOYEE.EMP_ID BETWEEN 11010 AND 11020
