CREATE TABLE EMPLOYEE(
EMP_ID INT,
EMP_FNAME VARCHAR(30),
EMP_LNAME VARCHAR(30),
EMP_BIRTH DATE,
EMP_REIGON VARCHAR(20), 
EMP_PHONE INT,
EMP_EMAIL VARCHAR(40),
JOB_ID INT,
PRIMARY KEY(EMP_ID)
);

CREATE TABLE JOB(
JOB_ID INT,
JOB_NAME VARCHAR(30),
JOB_RANK INT,
DEP_ID INT,
PRIMARY KEY(JOB_ID)
);

ALTER TABLE EMPLOYEE
	ADD FOREIGN KEY(JOB_ID) REFERENCES JOB ON UPDATE CASCADE ON DELETE CASCADE

CREATE TABLE JOB_HISTORY(
JOB_HISTORY_ID INT,
STARTS_DATE DATE,
TOP_TIME INT,
EMP_ID INT, 
JOB_ID INT,
PRIMARY KEY(JOB_HISTORY_ID),
FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE(EMP_ID) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY(JOB_ID) REFERENCES JOB ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE DEPARTMENT(
DEP_ID INT,
DEP_NAME VARCHAR(20), 
LOCATION_ID INT
PRIMARY KEY(DEP_ID)
);

ALTER TABLE JOB
	ADD FOREIGN KEY(DEP_ID) REFERENCES DEPARTMENT ON UPDATE NO ACTION ON DELETE NO ACTION

CREATE TABLE LOCATIONS(
LOCATION_ID INT,
STREET_NAME VARCHAR(30),
POSTAL_CODE INT,
CITY VARCHAR(30),
COUNTRY VARCHAR(20),
PRIMARY KEY(LOCATION_ID)
);

ALTER TABLE DEPARTMENT
	ADD FOREIGN KEY(LOCATION_ID) REFERENCES LOCATIONS ON UPDATE CASCADE ON DELETE CASCADE

CREATE TABLE SALARY(
SALARY_ID INT,
MIN_SALARY MONEY,
SALARY_NUM MONEY,
SALARY_TIME DATE,
JOB_ID INT,
PRIMARY KEY(SALARY_ID),
FOREIGN KEY(JOB_ID) REFERENCES JOB ON UPDATE CASCADE ON DELETE CASCADE
);


CREATE TABLE EXP_DO(
EXP_DO_ID INT,
EXP_JOB_ID INT,
EMP_ID INT,
PRIMARY KEY(EXP_DO_ID),
FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE EXP_JOB(
EXP_JOB_ID INT,
EXP_JOB_COMPANY VARCHAR(30),
EXP_JOB_NAME VARCHAR(30),
EXP_JOB_DEP VARCHAR(20),
PRIMARY KEY(EXP_JOB_ID)
);

ALTER TABLE EXP_DO
	ADD FOREIGN KEY(EXP_JOB_ID) REFERENCES EXP_JOB ON UPDATE CASCADE ON DELETE CASCADE

CREATE TABLE EXP_SCHOOL(
EXP_SCHOOL_ID INT,
EMP_ID INT,
ELEMENTARY VARCHAR(30),
JUNIOR VARCHAR(30),
SENIOR VARCHAR(30),
COLLEGE VARCHAR(30),
GRADUATE_SCHOOL VARCHAR(30),
PRIMARY KEY(EXP_SCHOOL_ID),
FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE LIENSE(
LIENSE_ID INT,
LIENSE_NAME VARCHAR(30),
PRIMARY KEY(LIENSE_ID)
);

CREATE TABLE EXP_LIENSE(
EXP_LIENSE_ID INT,
EMP_ID INT,
LIENSE_ID INT,
PRIMARY KEY(EXP_LIENSE_ID),
FOREIGN KEY(LIENSE_ID) REFERENCES LIENSE ON UPDATE NO ACTION ON DELETE NO ACTION,
FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE ASSESSMENT(
ASSESS_ID INT,
EMP_ID INT,
ASSESS_TIME DATE,
ASSESS_SCORE INT,
ASSESS_COMMENTS VARCHAR(50),
PRIMARY KEY(ASSESS_ID),
FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE ON UPDATE CASCADE ON DELETE CASCADE
);


